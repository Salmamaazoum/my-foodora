package food;

public class BadMealCompositionCreationException extends Exception{
	public BadMealCompositionCreationException() {
	}

	public BadMealCompositionCreationException(String arg0) {
		super(arg0);
	}

	public BadMealCompositionCreationException(Throwable arg0) {
		super(arg0);
	}

	public BadMealCompositionCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
